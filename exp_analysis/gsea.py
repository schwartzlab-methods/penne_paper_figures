'''
Run GSEA using the csv generated by deg_analysis
'''
import pandas as pd
import gseapy as gp
import numpy as np
import argparse
import os
import matplotlib.pyplot as plt
import seaborn as sns


def main():
    parser = argparse.ArgumentParser(description="GSEA analysis")
    parser.add_argument('--csv', type=str, required=True, help='Path to the csv file with LFC values')
    parser.add_argument('--output', type=str, required=True, help='Directory to save the GSEA results')
    parser.add_argument('--mapping_dict', type=str, default=None, help='features.tsv file for converting gene names to gene symbols.')
    parser.add_argument('--gene_sets', type=str, nargs="+", default=["MSigDB_Hallmark_2020"], 
                        help='Path to the gene sets file (GMT format or a string with names from GSEA). ' \
                        'If not provided, will use the default of MSigDB_Hallmark_2020.')
    args = parser.parse_args()
    if not os.path.exists(args.output):
        os.makedirs(args.output)

    # Load the csv file
    df = pd.read_csv(args.csv)
    # sort by metrics values (-log p-value * sign)
    df["score"] = -np.log10(df['p_value'] + 1e-10) * np.sign(df['log_fc'])
    df = df.sort_values(by='score', ascending=False)
    df = df.loc[:, ['gene', 'score']]
    # df.columns = ['gene', 'score']

    if args.mapping_dict:
        # Load the mapping dictionary
        template = pd.read_csv(args.mapping_dict, sep='\t')
        name_to_symbol = {row.iloc[0]: row.iloc[1] for _, row in template.iterrows()}  # map from gene symbol to name
        # Convert the gene names to gene symbols
        df['gene'] = df['gene'].map(name_to_symbol)
        # Drop rows with NaN values in the 'gene' column
        df = df.dropna(subset=['gene'])
        print("Size of the dataframe:", df.shape)        

    pre_res = gp.prerank(
        rnk=df,
        gene_sets=args.gene_sets,
        threads=4,
        min_size=1,
        max_size=1000,
        outdir=args.output,
        format='png',
        permutation_num=10000,
        seed=42,
        verbose=True
    )

    # Sort by NES and select top 10 UP enriched
    top_terms_sorted = pre_res.res2d.sort_values("NES", ascending=False)
    top_terms_sorted = top_terms_sorted[top_terms_sorted["FDR q-val"] < 0.05]
    top_terms_sorted = top_terms_sorted[top_terms_sorted["NES"] > 0].head(10)

    plt.figure(figsize=(10, 6))
    sns.barplot(
        x="NES",
        y="Term",
        data=top_terms_sorted,
        orient='h'
    )
    plt.title("Normalized Enrichment Scores (Top GSEA Hits)")

    plt.xlabel("Normalized Enrichment Score (NES)")
    plt.ylabel("Gene Set")
    plt.title("Top Positively Enriched Gene Sets")
    plt.tight_layout()
    plt.savefig(os.path.join(args.output, 'gsea_results_top.png'))
    plt.close()

    # Sort by NES and select top 10 DOWN enriched
    btm_terms_sorted = pre_res.res2d.sort_values("NES", ascending=False)
    btm_terms_sorted = btm_terms_sorted[btm_terms_sorted["FDR q-val"] < 0.05]
    btm_terms_sorted = btm_terms_sorted[btm_terms_sorted["NES"] < 0].head(10)

    plt.figure(figsize=(10, 6))
    sns.barplot(
        x="NES",
        y="Term",
        data=btm_terms_sorted,
        orient='h'
    )
    # plt.axvline(0, color='gray', linestyle='--')
    plt.title("Normalized Enrichment Scores (Top GSEA Hits)")

    plt.xlabel("Normalized Enrichment Score (NES)")
    plt.ylabel("Gene Set")
    plt.title("Top Negatively Enriched Gene Sets")
    plt.tight_layout()
    plt.savefig(os.path.join(args.output, 'gsea_results_btm.png'))
    plt.close()

if __name__ == "__main__":
    main()